class FloatDemo{

    public static void main(String[] args){

        float x = 5.5f;
        float y = 5;

        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x * y);
        System.out.println(x / y);
        System.out.println(x % y);


    }
}
