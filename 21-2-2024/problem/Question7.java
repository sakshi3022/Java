class Question7{

    public static void main (String[] args){

        for(char ch = 'a'; ch <= 'z'; ch++){
        System.out.println(ch);
        }

        for(char ch1 = 'A'; ch1 <='Z' ;ch1++){
            System.out.println(ch1);
        }
        
    }
}