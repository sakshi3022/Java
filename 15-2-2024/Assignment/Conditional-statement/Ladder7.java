class Ladder7{

    public static void main (String[] args){

        char ch = 'A';

        if ( ch == 12){
            System.out.println("A is value is 12");
        }else if (ch == 30){
            System.out.println("A is value is 30");
        }else if (ch == 48){
            System.out.println("A is value is 48");

        }else if (ch == 55){
            System.out.println("A is value is 55");

        }else if (ch == 65){
            System.out.println("A is value is 65");

        }

        else {
            System.out.println("The A value is not defined");
        }

 
     
	}
    }
