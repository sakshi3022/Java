class ComparisionP2{

    public static void main (String[] args){

        int a = 23;
        float b = 23.5f;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);


    }
}