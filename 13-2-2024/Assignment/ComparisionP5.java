class ComparisionP5{

    public static void main (String[] args){

        char a = 'A';  // A value in table is 12
        char b = 'B';  //B value in table is 13
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

        System.out.println(a);

    }
}