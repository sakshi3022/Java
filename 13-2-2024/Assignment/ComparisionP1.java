class ComparisionP1 {

    public static void main (String[] args) {

        System.out.println(16 > 12);
        System.out.println(16 < 12);
        System.out.println(16 >= 12);
        System.out.println(16 <= 12);
        System.out.println(16 == 12);
        System.out.println(16 != 12);



    }
}